export async function generateMetadata() {
  const locale = await getRequestLocale();

  return buildRouteMetadata({
    title: getLocaleCopy(locale, {
      en: "Privacy policy | Montelar",
      ru: "Политика конфиденциальности | Montelar",
    }),
    description: getLocaleCopy(locale, {
      en: "Montelar personal data processing and privacy policy for website visitors and consultation requests.",
      ru: "Политика обработки персональных данных Montelar для посетителей сайта и заявок на консультацию.",
    }),
    path: "/privacy",
    locale,
  });
}

